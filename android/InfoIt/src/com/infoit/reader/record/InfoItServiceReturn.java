package com.infoit.reader.record;

public class InfoItServiceReturn {
	private String mLocationName;
	private String mLocationUrl;
	private String mLocationThumbnailUrl;
	private String mYelpIdentifier;
	private String mYelpRating;
	private String mFoursquareIdentifier;
	
	public String getmLocationName() {
		return mLocationName;
	}
	public void setmLocationName(String mLocationName) {
		this.mLocationName = mLocationName;
	}
	public String getmLocationUrl() {
		return mLocationUrl;
	}
	public void setmLocationUrl(String mLocationUrl) {
		this.mLocationUrl = mLocationUrl;
	}
	public String getmLocationThumbnailUrl() {
		return mLocationThumbnailUrl;
	}
	public void setmLocationThumbnailUrl(String mLocationThumbnailUrl) {
		this.mLocationThumbnailUrl = mLocationThumbnailUrl;
	}
	public String getmYelpIdentifier() {
		return mYelpIdentifier;
	}
	public void setmYelpIdentifier(String mYelpIdentifier) {
		this.mYelpIdentifier = mYelpIdentifier;
	}
	public String getmYelpRating() {
		return mYelpRating;
	}
	public void setmYelpRating(String mYelpRating) {
		this.mYelpRating = mYelpRating;
	}
	public String getmFoursquareIdentifier() {
		return mFoursquareIdentifier;
	}
	public void setmFoursquareIdentifier(String mFoursquareIdentifier) {
		this.mFoursquareIdentifier = mFoursquareIdentifier;
	}
	
	
}
